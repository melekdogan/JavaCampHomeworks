
public class CreditCard {

 String cardName;
 String cardNumber;
 String expiration;
 String cvcCode;
 String country;
 String zipCode;
 
 public CreditCard(String cardName,String cardNumber, String expiration, String cvcCode, String country, String zipCode) {
	 this.cardName=cardName;
	 this.cardNumber=cardNumber;
	 this.expiration=expiration;
	 this.cvcCode=cvcCode;
	 this.country=country;
	 this.zipCode=zipCode;
 }

}
